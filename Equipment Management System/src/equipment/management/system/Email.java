   private final String GmailAccountEmail = "baosonprogramtest@gmail.com";
   private final String GmailPassword = "programtest123";
   
    public void SendMail() {
    Properties prop = System.getProperties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "465");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.socketFactory.port", "465");
        prop.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
        
        
                
        Session session = null;
        session = Session.getInstance(prop, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(GmailAccountEmail, GmailPassword); 
            }
            });
    try{
     MimeMessage message;
        message = new MimeMessage(session);

     message.setFrom(new InternetAddress(GmailAccountEmail));

     message.addRecipient(Message.RecipientType.TO,
                              new InternetAddress(StudentEmailTextField.getText()));
                              
     message.setSubject("Your Borrowed Item is Due In TODAY");
      
     message.setText("Dear Student \n The item you have borrowed is expected by Ms. Goppert to be return today! Please return it on time");

     Transport.send(message);
     JFrame frame = new JFrame("JOptionPane showMessageDialog example");
     JOptionPane.showMessageDialog(frame,
        "Email Sent Successful ",
        "",
        JOptionPane.INFORMATION_MESSAGE);;
    }catch (MessagingException mex) {
     mex.printStackTrace();
    }
   }

