    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sqlInsertNewEquipment = "INSERT INTO Equipment(EquipmentCode, EquipmentName, EquipmentDescription, Quantity, Supplier, Classroom) VALUES (?,?,?,?,?,?)";
        try {
            preparedStatement = connection.prepareStatement(sqlInsertNewEquipment);
            preparedStatement.setString(1, EquipmentCodeTextField.getText());
            preparedStatement.setString(2, EquipNameTextField.getText());
            preparedStatement.setString(3, EquipmentDescriptionTextField.getText());
            preparedStatement.setString(4, QuantityTextField.getText());
            preparedStatement.setString(5, SupplierTextField.getText());
            preparedStatement.setString(6, ClassroomTextField.getText());
            preparedStatement.execute();
            JOptionPane.showMessageDialog(null, "New Equipment Added Successfully");
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Failed to create new equipment. Please contact with teacher");
                 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

